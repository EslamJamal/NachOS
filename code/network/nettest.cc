/* Here we just send from mahcine ID 0 and then wait for a msg from the last one
   For other machine IDs we just receive the data sent and then send it back to the 
   farAddr */
void
MailTest_ring (int farAddr){

    PacketHeader outPktHdr, inPktHdr;
    MailHeader outMailHdr, inMailHdr;
    char data [40];
    char buffer[MaxMailSize];

    // construct packet, mail header for original message
    // To: destination machine, mailbox 0
    // From: our machine, reply to: mailbox 1

    // We check the value of farAddr if 0 we initialize the data msg and then send it 
    if (farAddr == 1){
        // this will be the firt message which is sent from machine 0 to 1
        strcpy(data, "Monkey Safari");
        outPktHdr.to = farAddr;     
        outMailHdr.to = 0;
        outMailHdr.from = 1;
        outMailHdr.length = strlen(data) + 1;

        // Send the first message
        postOffice->Send(outPktHdr, outMailHdr, data); 
    }

    // Wait for the first message from the other machine
    postOffice->Receive(0, &inPktHdr, &inMailHdr, buffer);
    printf("Got \"%s\" from %d, box %d\n",buffer,inPktHdr.from,inMailHdr.from);
    fflush(stdout);

     // sent from any machine    
    if (farAddr != 1){
        // get the data from the mailbox (using the buffer) and resend it to the farAddr
        strcpy(data, buffer);
        outPktHdr.to = farAddr;
        outMailHdr.to = 0;
        outMailHdr.from = 1;
        outMailHdr.length = strlen(data) + 1;

        //now send it to the farAddr
        postOffice->Send(outPktHdr, outMailHdr, data);
    }

    
    // Then we're done!
    interrupt->Halt();

}



